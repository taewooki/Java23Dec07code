package com.javalec.function;

public class AddSub extends Add {

	// Field

	// Constructor

	public AddSub() {

	}

	public int AddSub(int num1, int num2) {
		return num1 - num2;
	}

	public int AddSub(int num1, int num2, int num3) {
		return num1 - num2 - num3;

	}

	// Method

	public int AddSubResult1() {
		return num1 - num2 - num3;
	}

	public int AddSubResult2() {
		return num1 - num2;
	}

}
